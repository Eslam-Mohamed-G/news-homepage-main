function openNav() {
    document.getElementById("myNav").style.width = "65%";
    document.getElementById("body").style.cssText = "background-color: rgba(0,0,0, 0.3);";
}
  
function closeNav() {
    document.getElementById("myNav").style.width = "0%";
    document.getElementById("body").style.cssText = "background-color: rgba(255,255,255);";
}